#Escribir un programa que convierta un valor dado en grados Fahrenheit a grados Celsius. 
#Recordar que la fórmula para la conversión es: C = (F-32)*5/9

grados_Fahrenheit=int(input("Ingrese el valor e grados Fahrenheit:"))
grados_Celsius=(grados_Fahrenheit-32)*(5/9)
print("El valor en grados Celcius es:",grados_Celsius)


